package J3Classes.InheritanceClasses.J2MultilevelInheritance;

public class MultilevelInheritance {
    //Multilevel Inheritance
    //When a class extends a class, and that class extends another class, it is known as multilevel inheritance.
    //In multilevel inheritance, a class can inherit
    //the properties of a class, which in turn can inherit the properties of another class.
    //The syntax of multilevel inheritance is as follows:
    //class Subclass-name extends Superclass1-name
    //{
    //   //methods and fields
    //}
    //class Subclass-name extends Superclass2-name
    //{
    //   //methods and fields
    //}
    //Example of Multilevel Inheritance
    void multiLevel() {
        System.out.println("this is multilevel inheritance...");
    }
}
